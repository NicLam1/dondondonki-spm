-- Users and Tasks schema for Supabase (PostgreSQL)
-- Run in Supabase SQL editor or via CLI

-- Enable required extensions (safe to run multiple times)
create extension if not exists "uuid-ossp";

-- USERS table
create table if not exists public.users (
  user_id integer generated by default as identity primary key,
  email text not null unique,
  full_name text not null,
  role text not null check (role in ('STAFF','MANAGER','DIRECTOR')),
  access_level integer not null check (access_level in (0,1,2)),
  created_at timestamptz not null default now()
);

-- PROJECTS table (create this BEFORE tasks table to avoid foreign key reference errors)
create table if not exists public.projects (
  project_id integer generated by default as identity primary key,
  name text not null,
  description text,
  end_date date, -- deadline
  owner_id integer references public.users(user_id) on delete set null,
  tasks integer[] default '{}', -- array of task IDs related to this project
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- TASKS table
create table if not exists public.tasks (
  task_id integer generated by default as identity primary key,
  title text not null,
  description text,
  status text not null check (status in ('UNASSIGNED','ONGOING','UNDER_REVIEW','COMPLETED')) default 'UNASSIGNED',
  -- Priority bucket: integer 1â€“10 (1 = highest priority)
  priority_bucket integer not null check (priority_bucket between 1 and 10) default 5,
  due_date date,
  project text,
  owner_id integer references public.users(user_id) on delete set null,
  assignee_id integer references public.users(user_id) on delete set null,
  -- Note: column name kept as members_id per spec, stored as array to allow multiple members
  members_id integer[] default '{}',
  parent_task_id integer references public.tasks(task_id) on delete set null,
  -- attachments jsonb not null default '[]'::jsonb, -- e.g., [{"name":"spec.pdf","url":"..."}]
  is_deleted boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()

